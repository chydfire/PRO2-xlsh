function eveBytesAvailableCallbackFcn(t,events,handles)
global s;
global databuf;
datastrtmp = fread(s,1);
databuf = [databuf datastrtmp];
databuflen = length(databuf);
set(handles.listbox3,'String',databuf);
set(handles.axes1,'nextplot','add');
if(databuflen>1)
    plot(handles.axes1,[databuflen-1;databuflen],[databuf(databuflen-1);datastrtmp],'b');
end
if(databuflen<100)
    set(handles.axes1,'xlim',[0,100]);
else
    set(handles.axes1,'xlim',[databuflen-99,databuflen]);
end
if(mod(databuflen,100)==0)
    set(handles.edit4,'String',databuflen);
end






